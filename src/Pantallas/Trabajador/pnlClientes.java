    private void btnAceptarEditDirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarEditDirActionPerformed
        if(!txtProvincia.getText().isEmpty()&&!txtDepartamento.getText().isEmpty()
                && !txtdistrito.getText().isEmpty()&& !txtDireccion.getText().isEmpty()
                &&!txtReferencia.getText().isEmpty()){
            if(lblEditAddCliente.getText().equals("Agregar Registro")){
                if(btnAceptarEditDir.getText().equals("Agregar")){
                    agregarDirArray();
                    
                }else{
                    actualizarDirArray();
                    
                }
                listarDirArray();
                pnlDir.setPanelNormal(pnlTablaDir);
            }else{
                if(btnAceptarEditDir.getText().equals("Agregar")){                    
                    agregarDirQuery();
                                      
                }else{
                    actualizarDirQuery();

                }  
                listarDirQuery(txtDocumento.getText());
                pnlDir.setPanelNormal(pnlTablaDir);

            }
            limpiarPanelEditarDir();
        }else{
            JOptionPane.showMessageDialog(null,"Completa los campos animal ");
        }
    }//GEN-LAST:event_btnAceptarEditDirActionPerformed


