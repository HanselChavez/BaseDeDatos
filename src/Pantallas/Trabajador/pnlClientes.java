    private String generarCodigo(String dirActual) {
        
        int numeroActual = Integer.parseInt(dirActual.substring(4));  
        int siguienteNumero = numeroActual + 1; 
        String siguienteDireccion = "DIR-" + String.format("%03d", siguienteNumero);
    
        return siguienteDireccion;        
    }
    
