    private void btnEditClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditClienteActionPerformed
        //Obtener indice seleccionado
        int indice = tablaClientes.getSelectedRow();
        if(indice != -1)
        {
            Object objeto = tablaClientes.getValueAt(indice,0);
            modificarEditar();
            Servicios.llenarArrayCliente(objeto.toString());
            llenarPanelEditCliente(Arrays.arrCliente);
            listarDirQuery(objeto.toString());
            this.pnlPrincipal.setPanelNormal(pnlEditar);
            lblEditAddCliente.setText("Editar Registro");
            btnEditCliente.setVisible(false);
            btnAddCliente.setVisible(false);
        }
        else{
            System.out.println("Selecciona un registro");
        }
    }//GEN-LAST:event_btnEditClienteActionPerformed
